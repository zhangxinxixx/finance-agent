import assert from "node:assert/strict";
import { readFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const source = (relativePath) => readFileSync(join(root, relativePath), "utf8");
const adapter = source("src/adapters/liveStrategy.ts");
const hook = source("src/hooks/useLiveStrategy.ts");
const strategyHook = source("src/hooks/useStrategy.ts");
const page = source("src/pages/StrategyPage.tsx");
const workspace = source("src/components/strategy/LiveStrategyWorkspace.tsx");
const scenarios = source("src/components/strategy/LiveStrategyScenarios.tsx");
const types = source("src/types/live-strategy.ts");

assert.match(types, /schema_version: "live_strategy\.v1"/, "live strategy view model must pin the frozen schema version");
assert.match(adapter, /LIVE_STRATEGY_LATEST_PATH = "\/api\/live-strategy\/latest"/, "adapter must use the stable live strategy endpoint");
assert.match(adapter, /raw\.schema_version !== "live_strategy\.v1"/, "adapter must reject incompatible live strategy schemas");
assert.match(adapter, /export async function fetchLiveStrategy/, "adapter must export a dedicated live strategy fetcher");
assert.match(hook, /fetchLiveStrategy\(asset\)/, "live strategy hook must request the selected asset argument");
assert.match(hook, /15 \* 60_000/, "live strategy must refresh no more frequently than the agreed 15-minute cadence");
assert.match(strategyHook, /15 \* 60_000/, "daily strategy overview must refresh on the agreed 15-minute cadence");
assert.match(strategyHook, /historyPinnedRef/, "background refresh must preserve an explicitly selected history card");
assert.match(page, /<LiveStrategyWorkspace/, "Strategy page must mount the live strategy workspace");
assert.match(page, /tradeDate=\{data\.hero\.trade_date \?\? data\.trade_date\}/, "Strategy page must pass the latest daily trade date into the focused workspace");
assert.match(page, /activeView === "evaluation"/, "historical evaluation must load only on its dedicated page");
assert.match(page, /activeView === "evidence"/, "source trace must load only on its dedicated page");
assert.match(page, /liveStrategy\.refetch\(\)/, "Strategy page refresh must also refresh the live strategy workspace");
assert.match(workspace, /data\.live_market\.price/, "workspace must render backend canonical market price");
assert.match(workspace, /data\.strategy_status/, "workspace must render backend strategy status");
assert.match(workspace, /data\.market_state\.nearest_level/, "workspace must render backend nearest key level");
assert.match(workspace, /data\.market_state\.key_levels/, "workspace must render the backend structural key-level map");
assert.match(workspace, /今日关键价格地图/, "current strategy page must expose actionable structural levels instead of empty space");
assert.match(workspace, /今日观察清单/, "current strategy page must expose the current watch conditions");
assert.match(workspace, /data\.market_state\.level_event/, "workspace must render the backend level event without deriving it");
assert.match(workspace, /data\.market_state\.latest_price_event/, "workspace must render the backend latest price event");
assert.match(workspace, /data\.market_state\.confirmation_15m/, "workspace must render the backend 15m confirmation");
assert.match(workspace, /data\.feasibility\[key\]/, "workspace must render backend feasibility flags");
assert.match(page, /<LiveStrategyScenarios/, "daily strategy page must retain the read-only strategy scenarios when live data is available");
assert.match(scenarios, /setup\.entry_zone/, "scenario cards must display backend entry zones");
assert.match(scenarios, /setup\.stop_reference/, "scenario cards must display backend stops");
assert.match(scenarios, /setup\.risk_reward/, "scenario cards must display backend risk reward values");
assert.match(scenarios, /noTrade\.waiting_conditions/, "no-trade card must display backend waiting conditions");
assert.match(types, /"TRIGGERED"/, "live strategy status must support the additive triggered state");
assert.match(types, /active_scenario/, "live strategy view model must retain backend active scenario");
assert.match(types, /entry_zone/, "live strategy view model must retain backend entry zone");
assert.match(types, /stop_reference/, "live strategy view model must retain backend stop reference");
assert.match(types, /risk_reward/, "live strategy view model must retain backend risk reward");
assert.match(types, /waiting_conditions/, "live strategy view model must retain backend no-trade waiting conditions");
assert.match(adapter, /latest_price_event: normalizePriceEvent\(raw\.latest_price_event\)/, "adapter must preserve backend price event");
assert.match(adapter, /evidence: asEvidence\(raw\.evidence\)/, "adapter must preserve structured backend event evidence");
assert.match(adapter, /setups: Array\.isArray\(raw\.setups\)/, "adapter must preserve backend setups");
assert.match(adapter, /no_trade: normalizeNoTrade\(raw\.no_trade\)/, "adapter must preserve backend no-trade fields");
assert.match(adapter, /timestamps: asTimestampRecord\(raw\.timestamps\)/, "adapter must preserve backend market timestamps");
assert.match(adapter, /value: asNumber\(raw\.value\)/, "adapter must preserve backend nearest-level value");
assert.match(adapter, /reasons: asReasons\(raw\.reasons\)/, "adapter must preserve backend feasibility reasons by flag");
assert.match(page, /selectedAsset === "XAUUSD"/, "live strategy workspace must not contaminate non-XAUUSD strategy views");
assert.doesNotMatch(adapter, /touch_threshold|0\.2\s*\*|0\.05\s*\*/, "frontend adapter must not calculate live strategy thresholds");
assert.doesNotMatch(adapter, /Math\.(max|min)|touch_threshold|approach_threshold|break_buffer\s*=|retest_threshold\s*=|entry_buffer|stop_buffer/, "frontend adapter must not calculate event or risk parameters");
assert.doesNotMatch(workspace, /Math\.(max|min)|touch_threshold|approach_threshold|entry_buffer|stop_buffer/, "workspace must not calculate event or risk parameters");
assert.doesNotMatch(scenarios, /Math\.(max|min)|touch_threshold|approach_threshold|break_buffer\s*=|retest_threshold\s*=|entry_buffer|stop_buffer/, "scenario cards must not calculate event or risk parameters");

console.log("Live strategy frontend contract OK");
